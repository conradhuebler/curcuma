bool isElementSupported(const std::string& method, int Z)
{
    if (method == "mndo" || method == "MNDO") {
        return getMNDOParams().count(Z) > 0;
    } else if (method == "am1" || method == "AM1") {
        return getAM1Params().count(Z) > 0;
    } else if (method == "pm3" || method == "PM3") {
        return getPM3Params().count(Z) > 0;
    }
    return false;
}

int getCoreElectrons(int Z)
{
    if (Z <= 2) return 0;        // H, He
    if (Z <= 10) return 2;       // Li - Ne
    if (Z <= 18) return 10;      // Na - Ar
    if (Z <= 29) return 18;      // K  - Cu
    if (Z <= 36) return 28;      // Zn - Kr
    if (Z <= 47) return 36;      // Rb - Ag
    if (Z <= 54) return 46;      // Cd - Xe
    if (Z <= 57) return 54;      // Cs - La
    if (Z <= 79) return 68;      // Lu - Au
    if (Z <= 86) return 78;      // Hg - Rn
    return 78;                   // Fallback for heavier elements
}

